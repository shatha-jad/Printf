#include "ft_printf.h"

int	ft_putnbr(int n)
{
	int		i;

	i = 0;
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		i = 11;
	}
	else
	{
		if (n < 0)
		{
			i = ft_putchar('-');
			n = -n;
		}	
		if (n < 10)
			i += ft_putchar(n % 10 + '0');
		else
		{
			i += ft_putnbr(n / 10);
			i += ft_putnbr(n % 10);
		}
	}
	return (i);
}
